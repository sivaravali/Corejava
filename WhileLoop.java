package flowcontrols;
/**
 * 
 * @author sv22044
 * Print 1 to 10 numbers in reverse order
 * Initialisation
 *while(condition){
 *	statements;
 *	increment or decrement;
 }
 */

public class WhileLoop {

	public static void main(String[] args) {
		int i=10;
		while(i>=1) {
			System.out.println(i);
			--i;
		}

	}

}
